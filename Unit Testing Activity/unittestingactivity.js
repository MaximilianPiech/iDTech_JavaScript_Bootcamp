function Greet(name){
    console.log("Hello" + name + "!");
    if (name = null) {
        console.log("Hello, there!");
    }
    if (name == name.toUpperCase()) {
        console.log("HELLO" + name + "!");
    }
    if (name == Array()) {
        console.log("Hello" + "1," + "2," + "3," + "4,");
    }
}